export function noWhitespaceValidator(): ValidatorFn {
  return (control: AbstractControl): { [key: string]: any } | null => {
    const value: string = control.value;
    if (value && value.trim().length === 0) {
      return { whitespace: true };
    }
    return null;
  };
}
/*---------@function to constraint only letters in textfield------------*/
export function onlyLettersAllowed(event: any) {
  const pattern = /[a-zA-Z]/;  // Regular expression to allow only letters
  const inputChar = String.fromCharCode(event.charCode);

  if (!pattern.test(inputChar)) {
    event.preventDefault();  // Prevents the input character from being entered
  }
}
/*---------@function to constraint only numbers in textfield------------*/
export function onlyNumbersAllowed(event: any) {
  const pattern = /[0-9]/;  
  const inputChar = String.fromCharCode(event.charCode);

  if (!pattern.test(inputChar)) {
    event.preventDefault(); 
  }
}

/*---------@function to constraint only letters and space in textfield------------*/
export function onlyLettersAndSpaceAllowed(event: any) {
  const pattern = /[a-zA-Z]/;   
  const inputChar = String.fromCharCode(event.charCode);

  if (!pattern.test(inputChar)  && inputChar !== ' ') {
    event.preventDefault();  
  }
}
/*---------@function to constraint only alphanumeric and space in textfield------------*/
export function onlyAlphaNumericAllowed(event: any) {
  const pattern = /^[a-zA-Z0-9]+$/;  
  const inputChar = String.fromCharCode(event.charCode);

  if (!pattern.test(inputChar) && inputChar !== ' ') {
    event.preventDefault();  
  }
}


